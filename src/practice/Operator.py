# 숫자형 자료의 산술연산자 예제
a = 5
b = 2

print(f"{a} + {b} = {a + b}")   # 5와 2를 더한 값
print(f"{a} - {b} = {a - b}")   # 5에서 2를 뺀 값
print(f"{a} * {b} = {a * b}")   # 5와 2를 곱한 값
print(f"{a} / {b} = {a / b}")   # 5를 2로 나눈 값
print(f"{a} % {b} = {a % b}")   # 5를 2로 나눈 후 나머지 값
print(f"{a} // {b} = {a // b}") # 5를 2로 나눈 후 소수점을 버린 몫
print(f"{a} ** {b} = {a ** b}") # 5의 2제곱

# 할당 연산자 예제
c = 10
c += 5  # c = c + 5
print(f"c += 5 -> c = {c}")  # c의 값에 5를 더하고 할당

c -= 3  # c = c - 3
print(f"c -= 3 -> c = {c}")  # c의 값에서 3을 빼고 할당

c *= 2  # c = c * 2
print(f"c *= 2 -> c = {c}")  # c의 값에 2를 곱하고 할당

c /= 4  # c = c / 4
print(f"c /= 4 -> c = {c}")  # c의 값을 4로 나누고 할당

c //= 2  # c = c // 2
print(f"c //= 2 -> c = {c}")  # c의 값을 2로 나눈 후 소수점을 버린 몫을 할당

c %= 3  # c = c % 3
print(f"c %= 3 -> c = {c}")  # c의 값을 3으로 나눈 후 나머지를 할당

c **= 2  # c = c ** 2
print(f"c **= 2 -> c = {c}")  # c의 값을 제곱하여 할당
